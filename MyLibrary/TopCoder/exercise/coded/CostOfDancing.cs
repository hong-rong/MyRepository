using System;

public class CostOfDancing
{
    public int minimum(int K, int[] danceCost)
    {
        Array.Sort(danceCost);

        int sum = 0;
        for (int i = 0; i < K; i++)
        {
            sum = sum + danceCost[i];
        }

        return sum;
    }

